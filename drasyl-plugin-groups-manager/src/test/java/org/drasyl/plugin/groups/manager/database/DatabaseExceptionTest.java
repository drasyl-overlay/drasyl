package org.drasyl.plugin.groups.manager.database;

import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;

import static org.junit.jupiter.api.Assertions.assertEquals;

@ExtendWith(MockitoExtension.class)
class DatabaseExceptionTest {
    @Nested
    class Constructor {
        @Test
        void shouldBeConstructed() {
            final DatabaseException exception1 = new DatabaseException("error");
            final DatabaseException exception2 = new DatabaseException("error", new Exception());

            assertEquals("error", exception1.getMessage());
            assertEquals("error", exception2.getMessage());
        }
    }
}